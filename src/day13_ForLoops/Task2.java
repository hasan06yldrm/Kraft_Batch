package day13_ForLoops;

public class Task2 {
    public static void main(String[] args){
       //Örnek: 0 dan 100 e kadar olan çift sayıları ekrana yazdırın.
        for(int i=0;i<=100;i++){
            if(i%2==0){
                System.out.print(i+" ");
            }
        }
        System.out.println("-----------------------------------------------------");
        //Örnek: 0 dan 100 e kadar olan tek sayıları ekrana yazdırın.
        for(int i=0;i<=100;i++){
            if(i%2==1){
                System.out.print(i+" ");
            }
        }






    }
}
